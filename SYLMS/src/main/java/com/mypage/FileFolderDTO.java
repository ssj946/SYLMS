package com.mypage;

public class FileFolderDTO {
   private String id; 
   private int year;
   private String  studentCode; //학생코드 
   private String  subjectName; // 과목이름
   private String  fname; //파일이름 
   private String  submitDate; //등록일 
   private String  subjectNo; //과목번호
   private String fileNum; //파일번호
   private String originName; //원래이름
   private String saveName; //저장이름
   
   
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public int getYear() {
	return year;
}
public void setYear(int year) {
	this.year = year;
}
public String getStudentCode() {
	return studentCode;
}
public void setStudentCode(String studentCode) {
	this.studentCode = studentCode;
}
public String getSubjectName() {
	return subjectName;
}
public void setSubjectName(String subjectName) {
	this.subjectName = subjectName;
}
public String getFname() {
	return fname;
}
public void setFname(String fname) {
	this.fname = fname;
}
public String getSubmitDate() {
	return submitDate;
}
public void setSubmitDate(String submitDate) {
	this.submitDate = submitDate;
}
public String getSubjectNo() {
	return subjectNo;
}
public void setSubjectNo(String subjectNo) {
	this.subjectNo = subjectNo;
}
public String getOriginName() {
	return originName;
}
public void setOriginName(String originName) {
	this.originName = originName;
}
public String getSaveName() {
	return saveName;
}
public void setSaveName(String saveName) {
	this.saveName = saveName;
}
public String getFileNum() {
	return fileNum;
}
public void setFileNum(String fileNum) {
	this.fileNum = fileNum;
}

   
   
   
   
   
   
}
